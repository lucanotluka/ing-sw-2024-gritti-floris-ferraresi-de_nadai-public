package it.polimi.ingsw.model.cards;

/**
 * Enumeration representing different symbols for cards.
 *
 * @author Angelo De Nadai
 */
public enum Symbol {
    INSECT,      // Symbol representing an insect
    PLANT,       // Symbol representing a plant
    FUNGI,       // Symbol representing a fungi
    ANIMAL,      // Symbol representing an animal
    MANUSCRIPT,  // Symbol representing a manuscript
    INKWELL,     // Symbol representing an inkwell
    QUILL,       // Symbol representing a quill
    EMPTY        // Symbol representing an empty space
}
